	local name = GetHashKey("zr380")

	xPlayer.removeInventoryItem('zr380', 1)

	TriggerClientEvent('esx:spawnVehicle', source, name)
	TriggerClientEvent('esx:showNotification', source, 'spawned 1x zr380')
